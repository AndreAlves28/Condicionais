
package Condicionais;

public class Switch {

    public static void main(String[] args) {
        int x = 3;
        if(x == 1) {
            System.out.println("x equals 1");
        } else if (x == 2) {
            System.out.println("x equals 2");
        } else {
            System.out.println("No idea what x is");
        }
    }    
}
