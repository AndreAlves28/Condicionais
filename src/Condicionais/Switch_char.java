package Condicionais;

public class Switch_char {
    public static void main(String[] args) {
        char a = 'a';
        
        switch(a) {
            case 'a':
                System.out.println("555");
                break;
            case 'b':
                System.out.println('b');
                break;
            case 'c':
                System.out.println('c');
                break;
            default:
                System.out.println('d');
        }   
    }
}
